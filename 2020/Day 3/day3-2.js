function getNumTreesHit(right, down, hillMap) {
  let numTreesHit = 0;
  let vertPos = 0;
  let horizPos = 0;
  const hillWidth = hillMap[0].length;
  while(vertPos < hillMap.length) {
    horizPos %= hillWidth;
    if (hillMap[vertPos].charAt(horizPos) === '#') { numTreesHit += 1; }
    vertPos += down;
    horizPos += right;
  }
  return numTreesHit;
}
